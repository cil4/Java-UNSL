package Calculadora;

public class Calculadora {

	
	
	public float suma(float n1, float n2) {
		return n1 + n2;
		
	}
	
	public float resta(float n1, float n2) {
		return n1-n2;
	}
	
	public float multiplicacion(float n1, float n2) {
		return n1* n2;
	}
	
	public float division(float n1, float n2) {
		return n1/n2;
	}
}
