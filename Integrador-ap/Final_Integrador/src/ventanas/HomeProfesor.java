    public HomeProfesor(Map<String, String> data) {
        initComponents();
        con = new BD();
        con.Conectar();

        String pass = data.get("Key");

        try {
            result = con.ConsultaProf(pass);
            while (result.next()) {
                usuarioP.setText(result.getString("U_Nbre"));
                NombreP.setText(result.getString("P_Nbre"));
                passP.setText(result.getString("U_Key"));
                ApellidoP.setText(result.getString("P_Apell"));
                cuilP.setText(result.getString("P_CUIL"));
                telefP.setText(result.getString("P_Tel"));
                fotoP.setText(result.getString("P_Foto"));
                emailP.setText(result.getString("P_Email"));
                cargoCombo.setSelectedItem(result.getString("P_Cond"));
                cursoP.setSelectedItem(result.getString("M_Curso"));
                materiasP.setSelectedItem(result.getString("M_Nbre"));

            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        fotoP.setVisible(false);
        imgprofpath.setVisible(false);
        String imgpath= fotoP.getText();
        setFoto(imgpath);
        setLocationRelativeTo(null);
        this.dispose();
    }

