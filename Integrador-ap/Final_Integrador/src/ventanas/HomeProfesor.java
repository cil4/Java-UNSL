    private void botonConsultasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonConsultasActionPerformed
        String p_contrasenia = passP.getText();
        String p_curso = (String) cursoP.getSelectedItem();
        String p_cuil = cuilP.getText();
        
        Map<String, String> dataC = new HashMap<>();
        dataC.put("Key", p_contrasenia);
        dataC.put("p_curso", p_curso);
        dataC.put("p_cuil", p_cuil);

        ConsultasProfesores cp = new ConsultasProfesores(dataC);
        cp.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_botonConsultasActionPerformed

