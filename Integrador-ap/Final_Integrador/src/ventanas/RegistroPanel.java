    private void botonIniciarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIniciarRegistroActionPerformed
        RegistroEstudiante ventEst = new RegistroEstudiante();
        RegistroProfesor ventProf = new RegistroProfesor();
        if (tipoUsuarioRegistro.getSelectedItem() == "Estudiante") {
             
            ventEst.setVisible(true);
            this.dispose();
        } else {
             
            ventProf.setVisible(true);
            this.dispose();
        }


    }//GEN-LAST:event_botonIniciarRegistroActionPerformed

    private void tipoUsuarioRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tipoUsuarioRegistroActionPerformed
        if(tipoUsuarioRegistro.getSelectedItem()=="Profesor"){
             Image imgDocente = new ImageIcon(getClass().getResource("/Resources/paneldocente.png")).getImage();

        Image imgScale = imgDocente.getScaledInstance(imgpanel.getWidth(), imgpanel.getHeight(),
                Image.SCALE_SMOOTH);
        imgpanel.setIcon(new ImageIcon(imgScale)); 
        }else{
            Image imgEstud = new ImageIcon(getClass().getResource("/Resources/panelestudiante.png")).getImage();

        Image imgScale = imgEstud.getScaledInstance(imgpanel.getWidth(), imgpanel.getHeight(),
                Image.SCALE_SMOOTH);
        imgpanel.setIcon(new ImageIcon(imgScale));
        }
    }//GEN-LAST:event_tipoUsuarioRegistroActionPerformed

    public void setUsuario(String u) {
        if (u.equals("Estudiante")) {
            tipoUsuarioRegistro.setSelectedItem("Estudiante");
        } else {
            tipoUsuarioRegistro.setSelectedItem("Profesor");
        }

    }

