package EjercicioB1;

public class OperacionesFloat extends Operaciones{

	public float suma(float n1, float n2) {
		return n1 + n2;
		
	}
	
	public float suma(float n1, float n2, float n3) {
		return n1 + n2 + n3;
	}
	
	public float suma(float n1, float n2, float n3, float n4) {
		return n1 + n2 + n3 + n4;
	}
}
